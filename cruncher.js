function cruncher(numbers) {
	var translated = "";
	for(var i in numbers) {
		if(typeof numbers[i] === "string")
			numbers[i] = parseInt(numbers[i], 2);
		translated += String.fromCharCode(numbers[i]);
	}
	return translated;
}
